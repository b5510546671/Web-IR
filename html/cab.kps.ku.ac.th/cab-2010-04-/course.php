          <p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;คณาจารย์บัณฑิตจากภาควิชาและคณะต่างๆ ในมหาวิทยาลัยที่ปฏิบัติภารกิจร่วมกับศูนย์เห็นความจำเป็นที่จะต้องแสวงหาแนวทางที่จะผนวกกิจกรรมวิชาการวิทยาศาสตร์เกษตรกับความก้าวหน้าของวิทยาศาสตร์ชีวภาพแนวใหม่ โดยไม่สร้างผลกระทบต่อความเข้มแข็งของหลักสูตรระดับบัณฑิตในสาขาต่างๆ ของวิทยาศาสตร์เกษตร ซึ่งมหาวิทยาลัยดำเนินการอยู่แล้วมากกว่า 20 หลักสูตร ทางออกที่เห็นพ้องต้องกันคือ การสร้างหลักสูตรเทคโนโลยีชีวภาพเกษตรขึ้นใหม่อย่างมีเอกภาพและให้มีกลไกการบริหารเป็นการเฉพาะที่สามารถตอบสนองต่อแนวทางพัฒนาศูนย์ความเป็นเลิศของทบวง โดยระดมคณาจารย์จากหน่วยงานต่างๆ ร่วมกันสร้าง/สังเคราะห์รายวิชา และให้มีกลุ่มวิชาแกนในสาขาเทคโนโลยีจีโนมิคส์ ชีววิทยาโมเลกุล เซลล์วิทยา และสาขาใกล้เคียง เพื่อให้บัณฑิตที่สนใจงานวิจัยสาขาใดสาขาหนึ่งของวิทยาศาสตร์เกษตรสามารถนำความรู้และทักษะจากกลุ่มวิชาการทางเทคโนโลยีชีวภาพไปใช้ในงานวิจัยวิทยานิพนธ์ในสาขาวิทยาศาสตร์เกษตรที่ตนสนใจ การพัฒนาหลักสูตรดำเนินการควบคู่กับงานพัฒนาโครงสร้างพื้นฐานและการจัดหาครุภัณฑ์วิทยาศาสตร์ หลักสูตรสหวิทยาการระดับบัณฑิตศึกษาสาขาเทคโนโลยีชีวภาพได้เริ่มเปิดรับนิสิตเมื่อภาคต้นปีการศึกษา 2544 และประสบความสำเร็จอย่างดียิ่ง ภายในระยะอันสั้น ปัจจุบันหลักสูตรนี้เป็นหลักสูตรระดับบัณฑิตในกลุ่มสาขาวิทยาศาสตร์ชีวภาพที่มีจำนวนนิสิตบัณฑิตระดับปริญญาเอกมากที่สุดของประเทศ นิสิตส่วนใหญ่ของหลักสูตรมีฐานปฏิบัติงานศึกษาวิจัยอยู่ที่อาคารศูนย์ ซึ่งมีอุปกรณ์อำนวยความพร้อมทางการศึกษาวิจัยอย่างครบครันเทียบเท่ากับเกณฑ์มาตรฐานของสถาบันศึกษาชั้นนำของโลก</p>
          <p> &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ด้วยการสนับสนุนของบัณฑิตวิทยาลัย ศูนย์เทคโนโลยีชีวภาพเกษตรใช้หลักสูตรนี้เป็นเวทีทางวิชาการที่คณาจารย์และนิสิตจากต่างสาขาวิชาในกลุ่มวิทยาศาสตร์การเกษตรได้ศึกษา พบปะ หารือเพื่อแลกเปลี่ยนข้อคิดและพัฒนาทักษะก่อให้เกิดบรรยากาศและวัฒนธรรมทางวิชาการที่เอื้อต่อการศึกษาขั้นสูงเป็นอย่างยิ่ง</p>
