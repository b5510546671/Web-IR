                                      <p>จำนวนนักศึกษาบัณฑิต (2543 - ปัจจุบัน) ปริญญาโท 623 คน 
                                        ปริญญาเอก 177 คน </p>
                                      <p>จำนวนบัณฑิตที่จบการศึกษา (2543 - ปัจจุบัน) ปริญญาโท 378 คน 
