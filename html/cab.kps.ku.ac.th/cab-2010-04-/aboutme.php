                                    <p><strong>งานวิจัยที่เราเน้น</strong><br />
  &nbsp;&nbsp;&nbsp;&nbsp;ศูนย์ความเป็นเลิศด้านเทคโนโลยีชีวภาพเกษตร ดำเินินการวิจัยเชิงสหวิทยาการ ที่สามารถนำไปใช้เป็นเครื่องมือเพื่อเพิ่มประสิทธิภาพการผลิตภาคเกษตร ทางด้านพืช สัตว์ อาหารและพลังงาน โดยอาศัยการบูรณาการพื้นฐานปัญญาทางวิทยาศาสตร์เกษตรของไทยกับเทคโนโลยีชีวภาพ และวิทยาการชีววิทยาศาสตร์แนวใหม่ สามารถเจาะลึกถึงที่มาของปัญหานำมาซึ่งวิธีการแก้ปัญหา ที่ก่อให้เกิดสัมฤทธิผลดีกว่าวิธีวิจัยและการแก้ปัญหาแบบดั้งเดิมโดยมีองค์ประกอบ และโครงร้างการวิจัย ดังนี้</p>
                                    <p>&nbsp;&nbsp;&nbsp;&nbsp;เข้าถึงสาเหตุของปัญหาการผลิตในภาคเกษตร อาหาร และพลังงาน เจาะลึกพื้นฐาน และที่มาของปัญหาทางชีวภาพ ด้วยเทคโนโลยีจีโนมิคส์ ทำความเข้าใจในโครงสร้าง และการทำงานระดับดีเอ็นเอ เซลล์ เนื้อเยื้อ และชีววิทยาเชิงระบบ ใช้วิทยาการชีวฟิสิกส์ ชีวเคมี และสรีรวิทยาเชิงนิเวศ สนธิกับศาสตร์ทางการเกษตรทุกแขนง เพื่อให้เกิดความกระจ่างเรื่องปฏิสัมพันธ์ระหว่างปัจจัยทางชีวภาพและกายภาพ กลไกที่เป็นตัวควบคุมกระบวนการ และวิธีเพิ่มประสิทธิภาพการผลิต ในสภาพแวดล้อมที่มีความแตกต่างของแต่ละพื้นที่ <br />
                          <br />
&nbsp;&nbsp;&nbsp;                                      ใช้ความแม่นยำและความรวดเร็วของเทคโนโลยีชีวภาพ ระบุตำแหน่งโมเลกุลดีเอ็นเอเครื่องหมายในการคัดเลือก และพัฒนาพันธุ์ พืช สัตว์ จุลินทรีย์ ที่เหมาะกับสภาพแวดล้อม และสภาพการผลิตของระบบเกษตรกรรมไทย <br />
                          พัฒนาวิธีการ และชุดตรวจสอบโรคพืช โรคสัตว์ และความปลอดภัยทางอาหาร ที่มีความแม่นยำในระดับมาตรฐานโลก <br />
                          พัฒนาเทคโนโลยีที่ปลอดภัย ในการผนวกลักษณะเด่นทางพันธุกรรมของสิ่งมีชีวิตเพื่อการพัฒนาวัคซีน และสิ่งมีชีวิตดัดแปลงพันธุกรรม เพื่อเพิ่มความสามารถในการแข่งขันทางอุตสาหกรรม ผลิตภัณฑ์เกษตรและพลังงาน ในอนาคต <br />
                          จำแนกและอนุรักษ์ทรัพยากรทางชีวาภาพเพื่อนำไปใช้ประโยชน์อย่างยั่งยืน<br />
                          </p>                                    </td>
