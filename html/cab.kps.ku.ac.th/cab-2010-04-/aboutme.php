                                    <ul>
                                      <li>&nbsp;เพิ่มทุนการปัญญา ผดุงความยั่งยืนระบบเกษตรกรรม และความสามารถพึ่งพาตัวเองได้ของประชาคมวิทยาศาสตร์เกษตรไทย</li>
                                      <li>ปรับปรุงคุณภาพชีวิตของเกษตรกร</li>
                                      <li>&nbsp;ประกันความมั่นคงให้ผู้บริโภคให้ได้รับอาหาร และผลิตภัณฑ์จากภาคเกษตรที่มีคุณภาพอย่างเพียงพอ</li>
                                      <li>เพิ่มขีดความสามารถในการแข่งขันทุกระดับแก่ผู้ผลิตสินค้าเกษตร อาหาร</li>
