	<p>ศึกษาเกี่ยวกับพฤติกรรมมนุษย์ในการทำงานในองค์การทั้งภาครัฐและภาคเอกชน ปัจจัยทางจิตวิทยาที่เกี่ยวข้องกับการทำงาน การสรรหา การคัดเลือก การพัฒนาและบริหารทรัพยากรมนุษย์ในองค์การ</p>
	
	<p><strong><u>การประกอบอาชีพ</u></strong></p>
