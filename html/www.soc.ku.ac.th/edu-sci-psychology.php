	<h3>หลักสูตรวิทยาศาสตรบัณฑิต สาขาวิชาจิตวิทยา</h3>
	
	<p><strong>รับนักเรียนสายวิทยาศาสตร์ ประกอบด้วย 4 สาขาวิชา</strong></p>
	
	<hr class="hrdouble">
	
	<h4>สาขาวิชาจิตวิทยาชุมชน</h4>
	
	<p>ศึกษาเกี่ยวกับพฤติกรรมของบุคคลในชุมชน กลวิธีต่างๆ ในการป้องกันและส่งเสริมสุขภาพกายและสุขภาพจิต ปัจจัยต่างๆ
ที่เอื้อต่อการพัฒนาคุณภาพชีวิตและพัฒนาทรัพยากรบุคคล เพื่อความเข้มแข็งของชุมชน</p>
	<p><strong><u>การประกอบอาชีพ</u></strong></p>
	<p>ผู้ที่สำเร็จการศึกษาสาขาวิชาจิตวิทยาชุมชนสามารถประกอบอาชีพเป็นนักจิตวิทยา อาจารย์ นักวิจัย นักวิชาการและฝึกอบรม
	หรือตำแหน่งงานด้านมวลชนสัมพันธ์ ในหน่วยงานภาครัฐ และภาคเอกชน</p>
	
	<p><strong><u>การศึกษาต่อ</u></strong></p>
	<p>ผู้ที่สำเร็จการศึกษาสาขาวิขาจิตวิทยาชุมชนสามารถศึกษาต่อโดยตรงในสายจิตวิทยาชุมชนและในสายอื่นที่เกี่ยวข้อง</p>
	<hr>
		
		
	<h4>สาขาวิชาจิตวิทยาคลินิก</h4>
	<p>ศึกษาเกี่ยวกับการช่วยเหลือบุคคลที่มีปัญหาสุขภาพจิต ให้สามารถปรับความคิด พฤติกรรม และปรับตัวเข้า กับสังคมได้อย่างมีความสุข โดยเนื้อหาของการศึกษาจะประกอบไปด้วย การตรวจวินิจฉัยทางจิตวิทยา การบำบัดทางจิตวิทยาและการฟื้นฟูสมรรถภาพทางจิต เป็นต้น</p>
	
	<p><strong><u>การประกอบอาชีพ</u></strong></p> 
	<p>ผู้ที่สำเร็จการศึกษาสาขาวิชาจิตวิทยาคลินิกสามารถประกอบอาชีพเป็นนักจิตวิทยาคลินิกให้บริการด้านจิตเวชและสุขภาพจิต ทั้งภาครัฐและภาคเอกชน</p>
	
	<p><strong><u>การศึกษาต่อ</u></strong></p>
	<p>ผู้ที่สำเร็จการศึกษาสาขาวิชาจิตวิทยาคลินิกสามารถศึกษาต่อโดยตรงในสายจิตวิทยาคลินิก และสายอื่นที่เกี่ยวข้อง</p>	
	<hr>
	
	
	<h4>สาขาวิชาจิตวิทยาพัฒนาการ</h4>
	<p>ศึกษาเกี่ยวกับพัฒนาการของมนุษย์ตั้งแต่อยู่ในครรภ์มารดา วัยเด็ก วัยรุ่น วัยผู้ใหญ่ จนถึงวัยชรา รวมทั้งการเตรียมตัวตาย และเสริมสร้างพัฒนาการของบุคคลทุกช่วงวัย</p>
	
	<p><strong><u>การประกอบอาชีพ</u></strong></p>
