	<h4>ประวัติความเป็นมา</h4>
	
		<p>มหาวิทยาลัยเกษตรศาสตร์ ได้ทำการสอนวิชาด้านสังคมศาสตร์ ตั้งแต่ พ.ศ. 2490 เป็นต้นมา โดยรวมอยู่ในวิชาต่างๆ ของคณะเกษตรและคณะเศรษฐศาสตร์และบริหารธุรกิจ จนกระทั่งในปี พ.ศ. 2509 มหาวิทยาลัยเกษตรศาสตร์ ได้จัดตั้งคณะวิทยาศาสตร์และอักษรศาสตร์ขึ้น โดยมีภาควิชาสังคมศาสตร์และมนษุยศาสตร์เป็นภาควิชาในคณะนั้น เพื่อทำการสอนวิชาด้านสังคมศาสตร์ให้แก่นิสิตคณะต่างๆและในปี พ.ศ. 2510 ภาควิชาสังคมวิทยาและมนุษยศาสตร์ เริ่มรับนิสิตชั้นปีที่ 1 เข้าศึกษาในสาขาสังคมศาสตร์และมนุษยศาสตร์ ต่อมาภาควิชาสังคมศาสตร์และมนุษยศาสตร์ ได้รับการยกสถานะเป็น คณะสังคมศาสตร์ ในปี พ.ศ.2517</p>
		
		<hr>
		
		<h4>วัตถุประสงค์ของการจัดตั้ง</h4>
		
		<p>มหาวิทยาลัยเกษตรศาสตร์ ได้จัดตั้งคณะสังคมศาสตร์ โดยมีวัตถุประสงค์ ดังนี้<br>
			1. เพื่อให้การศึกษาด้านต่างๆในสายสังคมศาสตร์แก่นิสิตในมหาวิทยาลัยเกษตรศาสตร์<br>
			2. เพื่อผลิตนักวิชาการและนักวิจัยทางสังคมศาสตร์<br>
			3. เพื่อทำการศึกษา ค้นคว้า และวิจัยทางด้านสังคมศาสตร์ โดยเน้นการพัฒนาชนบทและการเกษตร<br>
			4. เพื่อให้บริการด้านการศึกษา และการวิจัยทางสังคมศาสตร์แก่ชุมชน
		</p>
		
		<hr>
		
		<h4>หลักสูตรที่เปิดสอน</h4>
		
		<p>คณะสังคมศาสตร์เปิดสอนทั้งภาคปกติและภาคพิเศษ (นอกเวลาราชการ) ระดับปริญญาตรี หลักสูตร 4 ปี 7 หลักสูตร รวม  14 สาขาวิชา ระดับปริญญาโท (บัณฑิตศึกษา) หลักสูตร 2 ปี 7 หลักสูตร รวม 10 สาขาวิชา และระดับปริญญาเอก (ดุษฎีบัณฑิต) หลักสูตร 3 ปี 1 หลักสูตร</p>
		
