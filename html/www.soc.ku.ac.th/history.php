     <p>ในปี พ.ศ. 2510 การจัดการเรียนการสอนด้านสังคมศาสตร์ ได้วางรากฐานอย่างมั่นคงในมหาวิทยาลัย เกษตรศาสตร์ โดยทางมหาวิทยาลัยได้อนุมัติให้ผลิตบัณฑิตหลักสูตร ศิลปศาสตรบัณฑิต (สาขาสังคมศาสตร์ทั่ว ไป) ในคณะวิทยาศาสตร์และอักษรศาสตร์ มีนิสิตคนแรกโอนมาจากคณะเกษตร และในปีการศึกษา 2510-11 ได้รับนิสิตเข้าศึกษารุ่นแรกโดยการสอบ จำนวน 12 คน มีคณาจารย์จำนวน 11 คน การจัดการเรียนการสอน เน้นหนักไปในการให้การศึกษาทางสังคมศาสตร์แก่นิสิตคณะต่างๆ ภาควิชาอื่นๆในคณะวิทยาศาสตร์และอักษรศาสตร์ด้วย</p>
     
