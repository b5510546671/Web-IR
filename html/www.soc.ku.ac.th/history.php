     <p> หลังจากได้รับอนุมัติให้จัดตั้งเป็นคณะสังคมศาสตร์ มหาวิทยาลัยได้แต่งตั้ง ดร.สุธรรม อารีกุล ซึ่งขณะนั้นดำรงตำแหน่งรอง อธิการบดีฝ่ายวิชาการมาเป็นผู้รักษา ราชการในตำแหน่ง คณบดีคณะสังคมศาสตร์ โดยขณะนั้นคณะสังคมศาสตร์มีทั้งหมด 5 ภาควิชาได้แก่</p>
     
     <ol>
     	<li>ภาควิชาจิตวิทยา</li>
