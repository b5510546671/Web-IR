		<li>ภาควิชาปรัชญาและศาสนา</li>
		<li>ภาควิชารัฐศาสตร์และรัฐประศาสนศาสตร์</li>
		<li>ภาควิชาสังคมวิทยาและมานุษยวิทยา</li>
     </ol>
     
     <p>ต่อมาในปี พ.ศ.2520 ได้รับอนุมัติให้จัดตั้งภาควิชา นิติศาสตร์ ขึ้นเนื่องจากมีความจำเป็นต้องเปิดสอนวิชากฎหมายเป็นวิชา พื้นฐานและวิชาเลือกให้แก่นิสิตทั่วไป โดยเฉพาะนิสิตที่เข้าศึกษาในหลักสูตรรัฐศาสตร์และรัฐประศาสนศาสตร์  ในปี พ.ศ.2523 มหาวิทยาลัยได้จัดตั้งคณะมนุษยศาสตร์ โดยรวมภาควิชาสายมนุษยศาสตร์ เช่น ภาษาต่างๆ ซึ่งเดิมอยู่ในคณะวิทยาศาสตร์และ อักษรศาสตร์ และภาควิชาปรัชญาและศาสนาจากคณะสังคมศาสตร์ เข้ารวมไว้กับคณะมนุษยศาสตร์ ต่อมา
คณะสังคมศาสตร์ก็ได้ รับอนุมัติจากมหาวิทยาลัยให้แยกภาควิชาภูมิศาสตร์และประวัติศาสตร์ ออกเป็นเอกเทศ เพื่อจะได้ดำเนินการจัดการเรียนการสอน โดยอิสระจากกัน โดยในปัจจุบัน คณะสังคมศาสตร์มีการจัดแบ่งโครงสร้างการบริหารดังนี้ </p>

