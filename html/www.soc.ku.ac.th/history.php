	<h2 class="text-center">ประวัติความเป็นมา</h2>
	
	<h3 class="text-center">ประวัติการก่อตั้ง</h3>
	<p>มหาวิทยาลัยเกษตรศาสตร์ได้เปิดสอนวิชาสังคมศาสตร์ตั้งแต่ พ.ศ. 2486 เป็นต้นมา พร้อมๆกับการก่อตั้ง มหาวิทยาลัยเกษตรศาสตร์ รายวิชาที่เปิดสอนในขณะนั้น ได้แก่ วิชากฎหมาย เปิดสอนอยู่ในคณะสหกรณ์ สังกัดแผนกวิชานิติศาสตร์ ส่วนรายวิชาด้านสังคมศาสตร์ก็เปิดสอนเพียงบางรายวิชา ได้แก่ สังคมวิทยาเบื้องต้น สังคมวิทยาชนบท แต่ยังไม่มีแผนกวิชาสังกัด อาจารย์ผู้สอน ล้วนแต่เป็นอาจารย์พิเศษทั้งสิ้น อาทิ ศาสตราจารย์ทำนอง สิงคาลวนิช, ดร.วิบูลย์ ธรรมวิทย์, สอนวิชาสังคมวิทยา พระชัยปัญญาสอนวิชากฎหมาย อาจารย์สุภร ผลชีวิน สอนวิชาภูมิศาสตร์เศรษฐกิจ</p>
	
