     <p>และในวันที่ 7 มีนาคม 2517 คณะรัฐมนตรี โดย ม.ร.ว.คึกฤทธิ์ ปราโมช เป็นนายกรัฐมนตรี ก็ได้อนุมัติให้จัดตั้ง คณะสังคมศาสตร์ ขึ้นใน มหาวิทยาลัยเกษตรศาสตร์ โดยมีวัตถุประสงค์ในการจัดตั้ง 4 ประการคือ</p>
     
     <ol>
     	<li>เพื่อให้การศึกษาด้านต่างๆในสายสังคมศาสตร์แก่นิสิตในมหาวิทยาลัยเกษตรศาสตร์</li>
		<li>เพื่อเป็นแหล่งผลิตนักวิจัยทางสังคมศาสตร์และสาขาวิชาอื่นๆที่เกี่ยวข้อง</li>
		<li>เพื่อทำการศึกษา ค้นคว้า และวิจัยทางด้านสังคมศาสตร์ โดยเน้นการพัฒนาสังคมและการเกษตร</li>
		<li>เพื่อบริการทางด้านวิชาการแก่สังคมโดยเฉพาะสายสังคมศาสตร์และสายอื่นๆที่เกี่ยวข้อง</li>
