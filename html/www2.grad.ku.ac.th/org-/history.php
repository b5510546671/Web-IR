                <br>
         จากการขยายตัวของการศึกษาขั้นบัณฑิตเป็นอย่างมาก                 ในเดือนสิงหาคม พ.ศ. 2505 มหาวิทยาลัยจึงได้แต่งตั้งคณะอนุกรรมการร่างข้อบังคับและร่างพระราชกฤษฎีกาจัดตั้ง                 บัณฑิตวิทยาลัย โดยมีศาสตราจารย์ ดร.ประเสริฐ ณ นคร เป็นประธานและอาจารย์สง่า                 สรรพศรี เป็นเลขานุการ<br>
<br>
         ในวันที่ 10                 ตุลาคม พ.ศ. 2509 มหาวิทยาลัยเกษตรศาสตร์ โดยอธิการบดี มล.ชูชาติ กำภู ได้มีคำสั่งแต่งตั้งให้                 ศาสตราจารย์ ดร.ประเสริฐ ณ นคร รองอธิการบดีฝ่ายวิชาการ รักษาการในตำแหน่งคณบดีบัณฑิตวิทยาลัย                 ดร.สง่า สรรพศรี อาจารย์เอกคณะวนศาสตร์เป็นรองคณบดี และอาจารย์บุญธรรม                 จิตต์อนันต์ อาจารย์โท คณะเกษตร เป็นเลขานุการบัณฑิตวิทยาลัย<br>
<br>
         งานบัณฑิตศึกษา                 ได้ขยายตัวต่อเนื่องและมีการเปิดสอนหลักสูตรบัณฑิตศึกษาในคณะต่าง ๆ                 มากขึ้นตามลำดับ จนกระทั่งในปี พ.ศ. 2520 จึงได้เริ่มเปิดสอนหลักสูตรดุษฎีบัณฑิต                 ในสาขาปฐพีวิทยาขึ้นเป็นหลักสูตรแรก<br>
<br>
